export function getRandomBlogs(arr: object[], x: number) {
    // Create a copy of the array to avoid modifying the original
    const copy = [...arr];
    const result = [];

    // Ensure x is not greater than the length of the array
    const numberOfItems = Math.min(x, copy.length);

    for (let i = 0; i < numberOfItems; i++) {
        // Generate a random index
        const randomIndex = Math.floor(Math.random() * copy.length);
        
        // Add the item at the random index to the result array
        result.push(copy[randomIndex]);
        
        // Remove the selected item from the copy to avoid duplicates
        copy.splice(randomIndex, 1);
    }

    return result;
}